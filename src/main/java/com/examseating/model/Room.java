package com.examseating.model;
public class Room {
    private String name; private int rows; private int cols;
    public Room() {}
    public String getName() { return name; } public void setName(String v) { this.name=v; }
    public int getRows() { return rows; } public void setRows(int v) { this.rows=v; }
    public int getCols() { return cols; } public void setCols(int v) { this.cols=v; }
}
